package javaWork2;

public class Course {
	String courseName;
	String educatorName;
	double courseStartDate;
	double courseEndDate;
	public Course ( String courseName, String educatorName,double courseStartDate, double courseEndDate  ) {
		this.courseName = courseName;
		this.educatorName = educatorName;	
		this.courseEndDate = courseEndDate;
		this.courseStartDate = courseStartDate;
	}
}